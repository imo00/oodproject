    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            // TODO add your handling code here:
            String flightID = flightid.getText();
            String flightName = flightname.getText();
            String Arrival = arrival.getText();
            String depurture = dep.getText();
            String duration = dur.getText();
            String seats = seat.getText();
            String Fare = fare.getText();
            String Dob = dob.getText();

            PreparedStatement pre;
            Class.forName("com.mysql.jdbc.Driver");
            Connection connection = DriverManager.getConnection("jdbc:mysql://localhost:3300/airline", "root", "6969");
            pre = connection.prepareStatement("insert into flight(flightID,flightName,arrival,departure,seats,fare,date,duration) values (?,?,?,?,?,?,?,?)");

            pre.setString(1, flightID);
            pre.setString(2, flightName);
            pre.setString(3, Arrival);
            pre.setString(4, depurture);
            pre.setString(5, seats);
            pre.setString(6, Fare);
            pre.setString(7, Dob);
            pre.setString(8, duration);

            pre.executeUpdate();

            JOptionPane.showMessageDialog(null, "FLIGHT ADDED SUCCESSFULLY");
            this.setVisible(false);
            AdminPanel admin = new AdminPanel();
            admin.setVisible(true);

        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(AddFlight.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

