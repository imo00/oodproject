    public void autoID() {

        try {
            Connection connection;
            PreparedStatement pre;
            Class.forName("com.mysql.cj.jdbc.Driver");
            connection = DriverManager.getConnection("jdbc:mysql://localhost:3300/airline", "root", "6969");
            pre = connection.prepareStatement("SELECT MAX(flightID) as maxID FROM flight");

            ResultSet resultSet = pre.executeQuery();
            if (resultSet.next()) {
                String maxID = resultSet.getString("maxID");
                if (maxID == null) {
                    flightid.setText("FL001");
                } else {
                    long id = Long.parseLong(maxID.substring(2));
                    id++;
                    flightid.setText("Fl" + String.format("%03d", id));
                }
            }
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(AddFlight.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

