    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            // TODO add your handling code here:
            String gender;
            if (male.isSelected()) {
                gender = "male";
            } else {
                gender = "female";
            }
            String customerID = cusID.getText();
            String firstName = firstname.getText();
            String lastName = lastname.getText();
            String passportNo = passNo.getText();
            String NID = nid.getText();
            String contact = contactNo.getText();
            String password = pass.getText();
            String username = user.getText();

            PreparedStatement pre;
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection connection = DriverManager.getConnection("jdbc:mysql://localhost:3300/airline", "root", "6969");
            pre = connection.prepareStatement("insert into customers(customerID,passportNO,firstName,lastName,nid,gender,contactNO,password,username) values (?,?,?,?,?,?,?,?,?)");
            pre.setString(1, customerID);
            pre.setString(2, passportNo);
            pre.setString(3, firstName);
            pre.setString(4, lastName);
            pre.setString(5, NID);
            pre.setString(6, gender);
            pre.setString(7, contact);
            pre.setString(8, password);
            pre.setString(9, username);

            pre.executeUpdate();
            JOptionPane.showMessageDialog(null, "Customer Added Succesfully");

            this.setVisible(false);
            CustomerLogin login = new CustomerLogin();
            login.setVisible(true);

        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(Registration.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

